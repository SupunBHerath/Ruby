# range of number

for i in 1..5
  puts "Current number is #{i}"
end

puts "loop finished\n\n"

fruits = ["apple","banana","cherry"]

for fruit in fruits 
  puts "Current fruit is #{fruit}"
end

puts "loop finished"


# outputs------
